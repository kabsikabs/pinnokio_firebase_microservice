# ──────────────────────────────────────
# Currency formatting
# ──────────────────────────────────────
def format_currency_ch(amount: float, currency: str = "CHF") -> str:
    """Swiss format: 1'234.56 CHF"""
    formatted = f"{amount:,.2f}".replace(",", "'")
    return f"{formatted} {currency}"


def format_currency_eu(amount: float, currency: str = "EUR") -> str:
    """EU format: 1 234,56 EUR"""
    formatted = f"{amount:,.2f}"
    formatted = formatted.replace(",", "TEMP").replace(".", ",").replace("TEMP", " ")
    return f"{formatted} {currency}"


def format_currency(amount: float, currency: str = "CHF") -> str:
    if currency in ("EUR",):
        return format_currency_eu(amount, currency)
    return format_currency_ch(amount, currency)


def format_date(d: str) -> str:
    """Format ISO date string to dd.mm.yyyy"""
    if not d:
        return ""
    try:
        parts = d.split("-")
        return f"{parts[2]}.{parts[1]}.{parts[0]}"
    except (IndexError, AttributeError):
        return str(d)


